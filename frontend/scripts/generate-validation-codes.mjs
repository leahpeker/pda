#!/usr/bin/env node
// Generates frontend/src/api/validationCodes.gen.ts from the backend's
// validation_codes.json. Run via `make generate-codes` (or indirectly via
// `make frontend-types`). The backend is the single source of truth —
// every PR that touches codes must regenerate both artifacts.
//
// `--check`: exits non-zero if the on-disk .gen.ts is out of date. Used in CI.

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const INPUT = resolve(__dirname, '../../backend/community/validation_codes.json');
const OUTPUT = resolve(__dirname, '../src/api/validationCodes.gen.ts');

const check = process.argv.includes('--check');

const catalog = JSON.parse(readFileSync(INPUT, 'utf8'));
const generated = renderFile(catalog);

if (check) {
  const existing = existsSync(OUTPUT) ? readFileSync(OUTPUT, 'utf8') : '';
  if (existing !== generated) {
    console.error('validationCodes.gen.ts is out of date — run `make generate-codes`.');
    process.exit(1);
  }
  console.log('validationCodes.gen.ts is up to date.');
  process.exit(0);
}

writeFileSync(OUTPUT, generated);
console.log(`wrote ${OUTPUT.replace(`${process.cwd()}/`, '')}`);

// ---

function renderFile(catalog) {
  const domains = Object.entries(catalog.domains);
  const header = `// @generated by frontend/scripts/generate-validation-codes.mjs
// Source: backend/community/_validation.py → backend/community/validation_codes.json
// Do not edit by hand — run \`make generate-codes\`.
//
// This file declares the wire-format code strings and a ValidationCode union.
// UI copy for these codes lives in validationCodes.ts (messageForCode).
`;

  const codeTree = renderCodeTree(domains);
  const union = renderUnion(domains);
  const paramsTable = renderParamsTable(domains);

  return `${header}
export const Code = ${codeTree} as const;

${union}

${paramsTable}
`;
}

function renderCodeTree(domains) {
  const lines = ['{'];
  for (const [domain, codes] of domains) {
    lines.push(`  ${domain}: {`);
    for (const { name, value } of codes) {
      lines.push(`    ${pascalToCamel(snakeToPascal(name))}: '${value}',`);
    }
    lines.push('  },');
  }
  lines.push('}');
  return lines.join('\n');
}

function renderUnion(domains) {
  const values = [];
  for (const [, codes] of domains) {
    for (const { value } of codes) {
      values.push(`'${value}'`);
    }
  }
  return `export type ValidationCode =\n  | ${values.join('\n  | ')};`;
}

// Emits a record of ``code -> declared param keys`` so tests/tooling can
// sanity-check that callers interpolate the params the backend actually sends.
function renderParamsTable(domains) {
  const lines = ['export const CODE_PARAMS: Record<ValidationCode, readonly string[]> = {'];
  for (const [, codes] of domains) {
    for (const { value, params } of codes) {
      const cleaned = params.map((p) => p.replace(/\?$/, ''));
      const list = cleaned.length === 0 ? '[]' : `[${cleaned.map((p) => `'${p}'`).join(', ')}]`;
      lines.push(`  '${value}': ${list},`);
    }
  }
  lines.push('};');
  return lines.join('\n');
}

function snakeToPascal(name) {
  return name
    .toLowerCase()
    .split('_')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join('');
}

// Top-level constants use PascalCase already (NotFound, TooLong) — keep it.
// Only exists so the conversion is explicit if we ever change casing.
function pascalToCamel(name) {
  return name;
}
